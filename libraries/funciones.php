 *  
 * */ 

function catalogo_x_juegos(string $nombre) :array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $m) {
        if($m['nombre'] == $nombre ){
            //agregar elementos al array (push)
            $resultado[] = $m;

        }

    }

    return $resultado;

}

/**
 * Devuelve los datos de un juego en particular
 */

 function producto_x_id($idnombre) {
    $catalogo = catalogo_completo();

    foreach ($catalogo as $m) {
        if($m['id'] == $idnombre){
            return $m;
        }
    }
    return null;
 }




